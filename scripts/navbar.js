function openBurgerMenu() {
    if(document.getElementById("regularMenu").style.display == "none") 
    {
        document.getElementById("regularMenu").style.display = "block";
    } 
    else 
    {
        document.getElementById("regularMenu").style.display = "none";
    }
}



